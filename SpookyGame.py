class Player():
    def __init__(self, x, y):
        self.images_right = []
        self.images_left = []
        self.index = 0
        self.counter = 0
        for num in range(0, 4):
            img_right = pygame.image.load(f'img/MazeRunner{num}.png')
            img_right =  pygame.transform.scale(img_right, (100,100))
            img_left = pygame.transform.flip(img_right, True, False)
            self.images_right.append(img_right)
            self.images_left.append(img_left)
        self.image = self.images_right[self.index]
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.width = self.image.get_width()
        self.height = self.image.get_height()
        self.vel_y = 0
        self.vel_x = 0
        self.direction = 0
        
    def update(self):
        dx = 0
        dy = 0
        walk_cooldown = 5

        key = pygame.key.get_pressed()
        if key[pygame.K_LEFT]:
            dx -= 5
            self.counter += 1
            self.direction = -1
        if key[pygame.K_RIGHT]:
            dx += 5
            self.counter += 1
            self.direction = 1
        if key[pygame.K_UP]:
            dy -= 5
            self.counter += 1
        if key[pygame.K_DOWN]:
            dy += 5
            self.counter += 1
        if key[pygame.K_LEFT] == False and key[pygame.K_RIGHT] == False and key[pygame.K_UP] == False and key[pygame.K_DOWN] == False:
                self.counter = 0
                self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]
        #animation
        if self.counter > walk_cooldown:
            self.counter = 0
            self.index += 1
            if self.index >= len(self.images_right):
                self.index = 0
            if self.direction == 1:
                self.image = self.images_right[self.index]
            if self.direction == -1:
                self.image = self.images_left[self.index]
        #check for collision
        for tile in world.tile_list:
            #collision in x direction
            if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                dx = 0

            #collision in y-direction
            if tile[1].colliderect(self.rect.x, self.rect.y +dy, self.width, self.height):
                dy = 0
        #update coodinates
        self.rect.x += dx
        self.rect.y += dy
        
        screen.blit(self.image, self.rect)
        


class World():
    def __init__(self, data):
        self.tile_list = []

        ground_img = pygame.image.load('img/floor.png')
        wall_img = pygame.image.load('img/wall.png')
        ceiling_img = pygame.image.load('img/ceiling.png')
        corner_img = pygame.image.load('img/corner.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile ==1:
                    img = pygame.transform.scale(ground_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile ==2:
                    img = pygame.transform.scale(wall_img, (tile_size, tile_size))
                    #img = pygame.transform.rotate(ground_img, 270)
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile ==3:
                    img = pygame.transform.scale(wall_img, (tile_size, tile_size))
                    img = pygame.transform.rotate(wall_img, 180)
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile ==4:
                    img = pygame.transform.scale(ceiling_img, (tile_size, tile_size))
                    #img = pygame.transform.rotate(ground_img, 180)
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1
    
    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])
